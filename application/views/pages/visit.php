	<h3><a name="finding"></a>Finding Library Materials</h3>
<img src="/assets/img/int4.jpg" class="int3 hidden-phone" alt="photograph of the interior of the special collections library"/>
<p>Books housed in Bruce Peel Special Collection are listed in the 
	<a href="https://neos.library.ualberta.ca/uhtbin/cgisirsi/x/0/0/57/49?user_id=WUAARCHIVE">University of Alberta Libraries' "Classic Catalogue."</a>
You can narrow your search to the Peel library by selecting “University of Alberta Bruce Peel Special Collections” under “Libraries.”  The 
library’s archival collections (i.e., photographs, correspondence, diaries, business records, etc.) are rarely listed in the library catalogue. 
Researchers can search for such materials using print or electronic finding aids. Further information related to <a href="/collections">research 
collections</a> in the Peel library is available in various formats, including online and in-house <a href="/exhibits">exhibitions</a>.  
If you require research assistance, please feel free to contact a member of the <a href="contact">library staff</a>.</p> 

<p>Theses and some other items housed in the Book and Record Depository (BARD) have “no-loan” status, which means that they cannot be checked out and removed from the library. However, these items can be requested for use in the Reading Room in the Peel library. Should you wish to access non-circulating material from the Book and Record Depository (BARD), please contact the Peel library at <a href="mailto:bpsc@ualberta.ca">bpsc@ualberta.ca</a> or drop-in to make arrangements for the transfer of non-circulating item(s) to the Peel library for your use.</p>

<h3><a name="using"></a>Using Library Materials</h3>

<p>Provided that they are requested at least 24 hours in advance using the <a href="https://docs.google.com/a/ualberta.ca/forms/d/126ZJ6r6L42AeoWVyOQRWdY9b0SYv7VxUljx6V1d56Po/edit">Retrieval Request Form</a>, the materials in the Peel library are available for use by students, staff, and the general public in the library’s supervised reading room upon presentation of valid photo identification (e.g., ONEcard, driver’s license, passport, or citizenship card with photo).</p> 
<p>For the security of researchers, staff, and the collection, there are security cameras throughout the space that are continuously monitored.</p> 
<p>There are no computer workstations in the Peel library’s reading room, but researchers are permitted to bring laptops with them. The University of Alberta’s wireless network is available in the library.</p> 

<p>Proper handling procedures must be followed at all times. Researchers should familiarize themselves with the Peel library’s <a href="#policy">Reading Room Policies</a> prior to arrival. If you have any questions about the policies, a staff member will be available to assist you during your visit, or you can <a href="contact">contact us</a> in advance.</p> 

<p>Researchers who wish to obtain reading copies for personal use may be permitted to take photographs (without flash) of library materials. Consult the Peel library’s <a href="#photos">Photography Policies</a> for more information. Positioning or arranging library materials in a manner that increases the risk of damage is never permitted. </p> 




<h3><a name="policy"></a>Reading Room Policies</h3><img src="/assets/img/int5.jpg" class="int2 hidden-phone" alt="photograph of the interior of the special collections library"/>

<p>The following are general rules for using library materials housed in the Peel library. For more specific guidelines for <a href="#books">handling books</a>,  <a href="#artbooks">artists’ books</a>, <a href="#archives">archives</a>, and <a href="#photos">photographic materials</a>, please follow the links.</p> 
<ul>
<li>The Gregory Javitch Reading Room is reserved for those consulting rare or archival materials.</li>
<li>Valid university or government-issued photo identification (e.g., ONEcard, driver’s license, passport, or citizenship card with photo) is required in order to access library materials. Photo identification will be held at the reference desk until all materials have been returned.</li> 
<li>No food (gum and candies included) or drink is permitted.</li> 
<li>All bags, briefcases, laptop sleeves, backpacks, etc. must be left at the reference desk. The library is not responsible for the loss of personal items, so please exercise care to safeguard personal property (e.g., do not bring valuables into the library). </li>  
<li>Library materials are to be handled with freshly washed hands (no hand sanitizer or lotion). Washrooms are located on the same level as the Peel library. In cases where gloves are required for handling they will be provided by library staff.</li>  
<li>Notebooks and laptops are permitted in the reading room. Please consult with library staff before taking additional research materials in.</li>  
<li>Pencils only may be used.  No ink of any kind will be permitted.</li> 
<li>Many library materials are fragile and require extra care while handling.  If you are unsure about proper handling procedures, please ask library staff for assistance.</li>   
<li>Library materials are not to be marked or altered in any way.</li> 
<li>Do not trace on any library materials or use any library materials as a pad for your paper.</li> 
<li>Please do not place items (notebooks, phones, tablets, etc.) on the library materials.</li> 
<li>Turn off audio and communication functions on all devices (laptops, cell phones, cameras, etc.) prior to entering the library. For persons with visual or hearing impairments, headphones and other assistive devices are permitted.</li> 
<li>Return all materials to the reference desk after use. Researchers who need to leave the reading room for more than 15 minutes should return materials to the reference desk.</li>  
<li>Please notify library staff if you plan on returning later in the week to further consult the materials as they may be placed on hold for you.</li> 
</ul>
<h3><a name="books"></a>Handling Books</h3>
<ul>
<li>All books are to be kept flat on the table or well supported in a book cradle. Do not allow books to hang over the table edge; do not stack them open on top of each other.  A book should never be held on your lap.</li> 
<li>Some books will not open flat due to the nature of their binding. Do not force these items flat. Book cradles and weights are available at the reference desk to help support and hold open the book. Please do not use other items to hold books open.</li>  
<li>Turn pages slowly and carefully. Do not thumb through the pages quickly as this may cause damage to fragile paper. Do not touch the illustrations inside books.</li> 
<li>All books and archival materials must remain within the sight of Reading Room staff at all times.</li> 

</ul>
<h3><a name="artbooks"></a>Handling Artists’ Books</h3>
<ul>
<li>Many artists’ books are stored in their own custom boxes; please note how they are arranged in their box as you remove them. If you feel you cannot safely return the item to its enclosure please ask for assistance from library staff.</li> 
<li>Some artists’ books are delicate and complicated. Library staff will unpack and set up these books for you. Do not attempt to put such items away yourself; please notify staff when you are finished.</li> 
<li>Always be cautious when handling artists’ books: they are works of art, and, due to design and composition, many lack the utilitarian strength of more standard books.</li>  
</ul>
<h3><a name="archives"></a>Handling Archives</h3>
<ul>
<li>Researchers working with archival materials will not be permitted to take their own paper or notebooks into the reading room, but they will be provided with coloured notepaper to use because it is easily distinguished from archival materials.</li>  
<li>When handling archival materials please remove only one folder at a time for research and return it to its box prior to removing another folder.</li>  
<li>Ensure that the original order of items in folders and boxes is maintained. Immediately inform library staff if you believe something is out of order or missing.</li> 
<li>Archival materials are very delicate and prone to damage during handling. Please take care when turning pages and when placing papers back into their folders. </li> 
<li>Ensure that no paper edges are exposed that may tear or chip when placing the folder back into its box.</li> 
<li>Many archival materials are housed in protective sleeves or enclosures. Please do not remove these materials from their protective housing.</li>  
</ul>
<h3><a name="photos"></a>Handling Photographic Materials</h3>
<p>Photographic materials include the following: encased photographs, lantern slides, glass plate negatives, negatives on film, black &amp; white and coloured photographic prints, etc. </p> 
<img src="/assets/img/int3.jpg" class="int3 hidden-phone" alt="photograph of the interior of the special collections library"/><ul>	
<li>Many of these items are housed in protective archival sleeves for preservation purposes. Please do not remove them from their sleeves.</li> 
<li>Gloves will be provided for handling photographic materials. </li> 
<li>Please handle all items along their edges and never touch the image surface as this will cause damage.</li>  
<li>Please handle albums carefully as many binding and pages have become weak over time. Never touch the surface of the prints, particularly with bare hands.</li>  
</ul>
<h3><a name="photopolicy"></a>Photography Policies</h3>

<p>Photography is permitted under the following conditions:</p>
<ul>
<li>Proper handling procedures, outlined in the <a href="#policy">Reading Room Policies</a>, must be followed at all times when photographing library materials.</li>  
<li>Flash photography is not permitted. Please ensure that your flash is disabled.</li>  
<li>Permission must be granted by library staff prior to any photography.</li>  
<li>Tripods, external flash devices, and other photographic equipment are not permitted in the reading room, nor are personal scanners, audio recorders, or video cameras.</li> 
<li>Photographs may not be taken of the library, other researchers, or library staff.</li>  
<li>Photographs may be taken only for the personal use of the researcher. For images intended for publication, researchers must submit a request for reproduction(s) to library staff. Permission for both photography and reproductions are subject to copyright, donor restrictions, and physical condition of the item(s).</li> 
<li>Researchers must ensure that they are not in violation of the <a href="http://laws-lois.justice.gc.ca/eng/acts/C-42/Index.html">Canadian Copyright Act</a> which outlines the standards for fair use by researchers.</li>  
</ul>
<h3><a name="reproduction"></a>Reproduction of Library Materials</h3>
<p>Photographs, scans, or photocopies are allowed on the condition that they will not damage rare materials, so we always 
	consider the condition of the original before giving permission for any kind of reproduction.</p>  
<p>We can provide high-resolution professional quality images at a cost of $20 each.  
	Contact Jeff Papineau (see <a herf="info/contact">Contacts</a>) 
	to be sure that your images are created to the exact specifications needed and to arrange for payment. 
	Researchers are welcome to take photographs themselves using a phone or camera without flash as a less 
	damaging and cheaper alternative to asking our staff to provide photocopies (50¢ each, cash only). </p> 

<p>If an image is needed for publication or exhibition use, it is necessary to first obtain permission from the 
	copyright owner, who is normally the author or creator. For more information, you can follow up with the 
	<a href="http://www.copyright.ualberta.ca/">University of Alberta Copyright Office</a></p>

<p>Libraries have traditionally charged a fee for the use of images of library materials. Like other U of A Libraries, 
	Bruce Peel Special Collections has waived usage fees on the condition that proper copyright permissions have been 
	obtained (if applicable) and that the following citation is displayed with the image: “Image courtesy of Bruce Peel 
	Special Collections, University of Alberta.”</p>

