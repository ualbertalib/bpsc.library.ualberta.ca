<h1>Bruce Peel Special Collections Workshops</h1>
<p class="top-margin">Bruce Peel Special Collections offers a series of online and in-person workshops each term to provide a wide range of learning opportunities to those University of Alberta students and employees who are motivated to sign up. These workshops explore a selection of topics that we have been teaching for years. The schedule/topics are posted well before the beginning of each term and registration opens on the first day of classes.</p>
