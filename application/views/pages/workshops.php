<h2>Bruce Peel Special Collections is reorganizing its teaching program</h2>
<p>Beginning in September 2018, the Peel library will offer a series of Peel Workshops each term to provide a wide range of hands-on and experiential learning opportunities to those University of Alberta students who are motivated to sign up. We receive far more requests for class visits than we can accommodate, so we came up with the idea of Peel Workshops as an alternative to such class visits. The workshops will explore generalized versions of the topics that we have been teaching, and the schedule/topics will be posted here well before the beginning of each term.</p>
<p><a href="https://docs.google.com/document/d/12H8TU7iKiayiwY4mYhzGYufmIXLkAUlZCUSKzVcoOtI/edit?usp=sharing" class="myButton">Peel Workshop Schedule</a></p>
<p><a href="http://ualberta.libcal.com/calendar/workshops/?cid=7360&t=d&d=0000-00-00&cal=7360" class="myButton">BPSC Workshop Registration &amp; Calendar</a></p>
<p class="sm-workshop">Registration for each Peel Workshop is limited to 15 participants, and is open to University of Alberta students (both graduate and undergraduate) in one or more departments, as indicated. Advance registration is required. Each student may sign up for a maximum of two Peel Workshops per term. Students who register and then find that they cannot attend should cancel their registration at least 24 hours in advance.</p>

